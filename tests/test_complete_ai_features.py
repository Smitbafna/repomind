def test_graph_rag(client: TestClient):
    """Test GraphRAG query."""
    # First, extract relationships (required for GraphRAG)
    print("\n📊 Extracting relationships for GraphRAG...")
    extract_response = client.post(f"/repositories/{REPO_ID}/relationships")
    if extract_response.status_code == 200:
        extract_data = extract_response.json()
        print(f"  Extracted {extract_data.get('relationships_extracted', 0)} relationships")
    
    # Now query GraphRAG
    response = client.post(
        f"/repositories/{REPO_ID}/graph/query",
        json={"question": "What are the main components?"}
    )
    assert response.status_code == 200
    data = response.json()
    # GraphRAG returns nodes and edges, answer is generated by agent
    assert "retrieved_nodes" in data
    assert "retrieved_edges" in data
    print(f"\n✅ GraphRAG query successful")
    print(f"  Nodes: {data['total_nodes']}, Edges: {data['total_edges']}")


