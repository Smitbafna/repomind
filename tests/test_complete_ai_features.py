"""Complete integration test for all AI features."""

from __future__ import annotations

import sys
from pathlib import Path

import pytest
from fastapi.testclient import TestClient

# Add project root to path
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from backend.api.main import app

REPO_ID = "01981d63-7bd7-459e-af26-d8a80617d499"


@pytest.fixture
def client() -> TestClient:
    """Create test client."""
    return TestClient(app)


def test_index_repository(client: TestClient):
    """Test indexing a repository."""
    response = client.post(f"/repositories/{REPO_ID}/index")
    assert response.status_code == 200
    data = response.json()
    assert data["status"] == "completed"
    assert data["documents_indexed"] > 0
    print(f"\n✅ Indexed {data['documents_indexed']} documents")


def test_vector_search(client: TestClient):
    """Test vector search."""
    response = client.post(
        f"/repositories/{REPO_ID}/query",
        json={"query": "authentication", "retriever": "vector", "top_k": 5}
    )
    assert response.status_code == 200
    data = response.json()
    assert data["total"] > 0
    assert len(data["results"]) > 0
    print(f"\n✅ Found {data['total']} vector search results")
    for i, result in enumerate(data['results'][:3], 1):
        print(f"  {i}. {result['file']} (score: {result['score']:.3f})")


